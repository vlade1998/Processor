ram[42] = {6'b011011,26'b00000000000000000000000000};

			pClock = 0;
		end
		if (we)
			ram[write_addr] <= data;
	end
	
	always @ (posedge read_clock)
	begin
		// Read 
		q <= ram[read_addr];
	end
	
endmodule
