ram[32] = {6'b010000,5'b00001,5'b11101,16'b0000000000000000};
ram[33] = {6'b001110,5'b00011,5'b11101,16'b0000000000000000};
ram[34] = {6'b001011,5'b10001,5'b00011,16'b0000000000000000};
